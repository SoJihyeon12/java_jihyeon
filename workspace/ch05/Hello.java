package ch05;

public class Hello {
    static void main() {
        int a = 10;
        int b = ++a;
        System.out.println(a); // a = 11
        System.out.println(b); // b = 11

        int c = 10;
        int d = c++;
        System.out.println(c); // c = 11
        System.out.println(d); // d = 10




    }

}
